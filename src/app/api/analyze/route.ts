// Helper function to extract JSON from AI response text
function extractJson(text: string): any {
  // Try to find code block patterns first
  const jsonMatch = text.match(/```(?:json)?\s*([\s\S]*?)\s*```/);
  const cleanText = jsonMatch ? jsonMatch[1].trim() : text.trim();
  
  try {
    return JSON.parse(cleanText);
  } catch (e) {
    // If that fails, try to search for the first '{' and last '}'
    const firstBrace = text.indexOf('{');
    const lastBrace = text.lastIndexOf('}');
    if (firstBrace !== -1 && lastBrace !== -1 && lastBrace > firstBrace) {
      const parsedText = text.substring(firstBrace, lastBrace + 1);
      return JSON.parse(parsedText);
    }
    throw e;
  }
}

