// Custom runtime validation for safety
function isValidApiResponse(json: any): json is ApiResponse {
  if (!json || typeof json !== 'object') return false;
  
  const { analysis, bonuses, bonusPage, emails } = json;
  
  if (!analysis || typeof analysis !== 'object') return false;
  if (typeof analysis.productName !== 'string') return false;
  if (typeof analysis.targetAudience !== 'string') return false;
  if (!Array.isArray(analysis.features)) return false;
  if (!Array.isArray(analysis.benefits)) return false;
  if (!Array.isArray(analysis.painPoints)) return false;
  if (typeof analysis.usp !== 'string') return false;
  if (!Array.isArray(analysis.marketingAngles)) return false;
  
  if (!Array.isArray(bonuses)) return false;
  // Ensure we have at least some bonuses (target 10, but let's be flexible to not fail if we get 8-10, though prompt asks for 10)
  if (bonuses.length === 0) return false;
  for (const b of bonuses) {
    if (typeof b.name !== 'string' || typeof b.description !== 'string' || typeof b.whyItHelps !== 'string' || typeof b.estimatedValue !== 'string') {
      return false;
    }
    if (typeof b.deliveryFormat !== 'string' || typeof b.conversionBenefit !== 'string' || typeof b.objectionSolved !== 'string') {
      return false;
    }
  }
  
  if (!bonusPage || typeof bonusPage !== 'object') return false;
  if (typeof bonusPage.headline !== 'string') return false;
  if (typeof bonusPage.subheadline !== 'string') return false;
  if (typeof bonusPage.ctaText !== 'string') return false;
  if (typeof bonusPage.bonusStackDescription !== 'string') return false;
  if (typeof bonusPage.scarcitySection !== 'string') return false;
  if (typeof bonusPage.closingSection !== 'string') return false;
  if (typeof bonusPage.htmlTemplate !== 'string') return false;
  
  if (!Array.isArray(emails)) return false;
  if (emails.length === 0) return false;
  for (const e of emails) {
    if (typeof e.type !== 'string' || typeof e.subject !== 'string' || typeof e.previewText !== 'string' || typeof e.body !== 'string' || typeof e.ctaText !== 'string') {
      return false;
    }
  }
  
  return true;
}

export async function POST(req: NextRequest) {
  try {
    const { url, fallbackContent, openRouterKey, selectedModel } = await req.json();

    let contentToAnalyze = "";

    // 1. Scrape with Jina Reader if URL is provided
    if (url && url.trim() !== "") {
      let targetUrl = url.trim();
      if (!/^https?:\/\//i.test(targetUrl)) {
        targetUrl = "https://" + targetUrl;
      }
      
      try {
        const jinaUrl = `https://r.jina.ai/${targetUrl}`;
        const response = await fetch(jinaUrl, {
          method: "GET",
          headers: {
            "Accept": "text/plain",
            "X-Return-Format": "markdown"
          },
          signal: AbortSignal.timeout(12000) // 12-second timeout for Jina
        });

        if (response.ok) {
          contentToAnalyze = await response.text();
        } else {
          console.warn(`Jina Reader failed with status: ${response.status}`);
        }
      } catch (scrapeErr) {
        console.error("Error scraping with Jina Reader:", scrapeErr);
      }
    }

    // 2. Fallback to manual content if scraping failed or was not requested
    if (!contentToAnalyze || contentToAnalyze.trim().length < 50) {
      if (fallbackContent && fallbackContent.trim().length >= 10) {
        contentToAnalyze = fallbackContent.trim();
      } else {
        return NextResponse.json(
          {
            error: "Scraping failed, and no fallback sales page content was pasted. Please enter sales page content manually.",
            scrapingFailed: true
          },
          { status: 400 }
        );
      }
    }

    // Truncate input text to avoid token overflow (around 15000 chars is fine for Gemini/DeepSeek)
    if (contentToAnalyze.length > 25000) {
      contentToAnalyze = contentToAnalyze.substring(0, 25000) + "... [Truncated]";
    }

    // 3. Setup OpenRouter call
    const apiKey = (openRouterKey && openRouterKey.trim() !== "") ? openRouterKey.trim() : process.env.OPENROUTER_API_KEY;
    const model = (selectedModel && selectedModel.trim() !== "") ? selectedModel.trim() : (process.env.OPENROUTER_MODEL || "google/gemini-2.5-flash");

    if (!apiKey) {
      return NextResponse.json(
        {
          error: "API Key Missing: Please provide your OpenRouter API Key in the settings panel, or configure OPENROUTER_API_KEY on the server."
        },
        { status: 400 }
      );
    }

    const systemPrompt = `You are a world-class affiliate marketer, copywriting expert, and SaaS product architect.
Analyze the provided sales page content and output a comprehensive affiliate promotion suite.

You MUST return your response as a single, valid JSON object matching the exact schema below. Do not output any introductory or explanatory text. Do not wrap the JSON in any markdown other than optionally a json code block.

JSON Schema:
{
  "analysis": {
    "productName": "Name of the product",
    "targetAudience": "Clear description of the ideal buyer",
    "features": ["Feature 1", "Feature 2", "Feature 3", "Feature 4", "Feature 5"],
    "benefits": ["Benefit 1", "Benefit 2", "Benefit 3", "Benefit 4", "Benefit 5"],
    "painPoints": ["Pain point 1 resolved", "Pain point 2 resolved", "Pain point 3 resolved"],
    "usp": "Unique Selling Proposition",
    "marketingAngles": ["Angle 1", "Angle 2", "Angle 3"]
  },
  "bonuses": [
    {
      "name": "Bonus 1 Name",
      "description": "Detailed description of what the buyer receives",
      "whyItHelps": "Explains how this bonus directly plugs a gap in the main product, resolves an objection, or helps the buyer implement it twice as fast",
      "estimatedValue": "$97",
      "deliveryFormat": "Must be one of: PDF, Video Course, Templates, Swipe File, Prompt Pack, Toolkit",
      "conversionBenefit": "A clear, compelling explanation of how this bonus increases their sales conversion or speeds up results",
      "objectionSolved": "The specific customer objection this bonus handles and eliminates (e.g. 'solves lack of technical skills concern')"
    }
  ],
  "bonusPage": {
    "headline": "Attention-grabbing, conversion-optimized headline for the bonus page",
    "subheadline": "Compelling subheadline outlining the value and limited-time offer",
    "ctaText": "Claim Your Product & Special Bonuses Now",
    "bonusStackDescription": "Summary highlighting the combined value of all 10 bonuses",
    "scarcitySection": "Urgent copy specifying that this offer is highly limited (e.g. expiring on date or limited to first 50 action takers)",
    "closingSection": "Reassuring final words eliminating risk, encouraging action, and outlining next steps",
    "htmlTemplate": "Leave this field as an empty string \"\". The backend will automatically generate the premium HTML structure using your copy."
  },
  "emails": [
    {
      "type": "announcement",
      "subject": "Announcement email subject line",
      "previewText": "Email teaser preview text",
      "body": "Email body copy. Use placeholders like [Name] and [Affiliate Link].",
      "ctaText": "Secure Your Copy + Bonuses Here"
    }
  ]
}

Constraints:
1. "bonuses" MUST contain exactly 10 highly relevant, high-value, complementary bonus ideas (NOT generic ones).
2. "emails" MUST contain exactly 4 emails with types: "announcement", "benefits", "faq", and "last-chance".
3. Every field must be populated with rich, high-quality, conversion-optimized text. No empty values or placeholders (except in the email body for names/links).`;

    const userPrompt = `Here is the scraped content from the product sales page:
---
${contentToAnalyze}
---

Generate the affiliate marketing suite JSON matching the specified schema. Make sure the 'htmlTemplate' inside 'bonusPage' is fully formatted, readable HTML ready for previewing.`;

    const messages = [
      { role: "system", content: systemPrompt },
      { role: "user", content: userPrompt }
    ];

    let apiResultText = "";
    let attempts = 0;
    const maxAttempts = 3;
    let parsedResult: any = null;

    while (attempts < maxAttempts) {
      attempts++;
      try {
        const response = await fetch("https://openrouter.ai/api/v1/chat/completions", {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            "Authorization": `Bearer ${apiKey}`,
            "HTTP-Referer": "https://github.com/google/antigravity", // fallback referer
            "X-Title": "Affiliate Bonus Generator"
          },
          body: JSON.stringify({
            model: model,
            messages: messages,
            response_format: { type: "json_object" },
            temperature: 0.3
          }),
          signal: AbortSignal.timeout(60000) // 60-second timeout for LLM
        });

        if (!response.ok) {
          const errText = await response.text();
          throw new Error(`OpenRouter API responded with ${response.status}: ${errText}`);
        }

        const data = await response.json();
        apiResultText = data.choices?.[0]?.message?.content || "";
        
        if (!apiResultText) {
          throw new Error("Empty content returned from OpenRouter");
        }

        // Try parsing JSON
        parsedResult = extractJson(apiResultText);

        // Populate HTML template dynamically on the server
        if (parsedResult && parsedResult.bonusPage && Array.isArray(parsedResult.bonuses)) {
          parsedResult.bonusPage.htmlTemplate = buildHtmlPage(
            parsedResult.analysis || {}, 
            parsedResult.bonuses, 
            parsedResult.bonusPage
          );
        }

        // Validate structure
        if (isValidApiResponse(parsedResult)) {
          break; // Parsing and validation succeeded, break loop
        } else {
          console.warn("Parsed object structure was invalid. Attempting correction...");
          throw new Error("Invalid structure returned according to schema requirements.");
        }

      } catch (err: any) {
        console.error(`Attempt ${attempts} failed:`, err.message);
        
        const isTimeout = err.name === 'TimeoutError' || err.message.includes('timeout') || err.message.includes('aborted');
        
        if (attempts >= maxAttempts || isTimeout) {
          return NextResponse.json(
            { 
              error: isTimeout 
                ? "OpenRouter API connection timed out. The request took longer than 60 seconds. Please try again or switch to Google Gemini 2.5 Flash."
                : `AI generation failed after multiple attempts. Error: ${err.message}`, 
              rawResponse: apiResultText 
            },
            { status: isTimeout ? 504 : 500 }
          );
        }

        // Only append to history if we actually got a response to correct.
        // If it was a network timeout or completely empty, we do not pollute the chat history.
        if (apiResultText && apiResultText.trim().length > 0) {
          messages.push({
            role: "assistant",
            content: apiResultText
          });
          messages.push({
            role: "user",
            content: `The response provided was invalid or failed JSON parsing. Error detail: ${err.message}. Please rewrite the response, ensuring that:
1. It is a strictly valid JSON object matching the exact schema.
2. No text exists outside of the JSON structure.
3. Every single field is correctly populated.
4. The "bonuses" array contains exactly 10 items.
5. The "emails" array contains exactly 4 items (announcement, benefits, faq, last-chance).
Ensure all trailing commas, unescaped characters, or broken blocks are corrected.`
          });
        }
      }
    }

    return NextResponse.json(parsedResult);

  } catch (error: any) {
    console.error("Unhandled error in API route:", error);
    return NextResponse.json(
      { error: `An unexpected server error occurred: ${error.message || error}` },
      { status: 500 }
    );
  }
}
